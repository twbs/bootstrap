$(function() {
  function init_panel() {
    $('.panel-heading.dropdown').each(function() {
        var h = $(this);
        var b = h.nextUntil('.panel-heading');
        $(this).click(function() {
            if (h.hasClass('dropup')) {
                b.slideDown(function() {
                    h.removeClass('dropup');
                });
            }
            else {
                b.slideUp(function() {
                    h.addClass('dropup');
                });
            }
        });
    });
  }

  init_panel();

});

